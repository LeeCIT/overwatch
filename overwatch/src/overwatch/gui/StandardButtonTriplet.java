


package overwatch.gui;

import javax.swing.JButton;





public class StandardButtonTriplet
{
	public final JButton addNew;
	public final JButton save;
	public final JButton delete;
	
		
	
	
	
	public StandardButtonTriplet( JButton addNew, JButton save, JButton delete )
	{
		this.addNew = addNew;
		this.save   = save;
		this.delete = delete;
	}	
}

